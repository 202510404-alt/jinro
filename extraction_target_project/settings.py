# ============================================================
# 🎥 Camera Tuning (Elastic / Dynamic Smoothing)
# ------------------------------------------------------------
# - CAMERA_SMOOTHING (0~1):
#   60FPS에서 "한 프레임에 목표로 얼마나 따라갈지"를 의미합니다.
#   값이 낮을수록 무겁고 느긋(카메라가 늦게 따라옴), 높을수록 즉각 반응합니다.
CAMERA_SMOOTHING = 0.12
CAMERA_DEADZONE_W = 180
